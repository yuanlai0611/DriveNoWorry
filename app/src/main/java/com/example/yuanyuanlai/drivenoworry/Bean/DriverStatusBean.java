package com.example.yuanyuanlai.drivenoworry.Bean;

public class DriverStatusBean {

    private int userId;
    private double temperature;
    private int heartRate;
    private int alcoholConcentration;
    private int diastolicPressure;
    private int systolicPressure;
    private int heartSatus;
    private int pressureStatus;
    private int alcoholStatus;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public double getTemperature() {
        return temperature;
    }

    public void setTemperature(double temperature) {
        this.temperature = temperature;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }

    public int getAlcoholConcentration() {
        return alcoholConcentration;
    }

    public void setAlcoholConcentration(int alcoholConcentration) {
        this.alcoholConcentration = alcoholConcentration;
    }

    public int getDiastolicPressure() {
        return diastolicPressure;
    }

    public void setDiastolicPressure(int diastolicPressure) {
        this.diastolicPressure = diastolicPressure;
    }

    public int getSystolicPressure() {
        return systolicPressure;
    }

    public void setSystolicPressure(int systolicPressure) {
        this.systolicPressure = systolicPressure;
    }

    public int getHeartSatus() {
        return heartSatus;
    }

    public void setHeartSatus(int heartSatus) {
        this.heartSatus = heartSatus;
    }

    public int getPressureStatus() {
        return pressureStatus;
    }

    public void setPressureStatus(int pressureStatus) {
        this.pressureStatus = pressureStatus;
    }

    public int getAlcoholStatus() {
        return alcoholStatus;
    }

    public void setAlcoholStatus(int alcoholStatus) {
        this.alcoholStatus = alcoholStatus;
    }

}
